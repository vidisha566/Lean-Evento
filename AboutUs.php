					<td><strong>¿Qué es LEAN?</strong><br>
							<p>LEAN es una asociación civil sin fines de lucro conformada por gente con<br> gran sensibilidad social, dedicada a la defensa de los derechos humanos<br> y la consecución de ayuda humanitaria, favoreciendo directamente o a<br> través de otras asociaciones o agrupaciones provinciales a<br> venezolanos residentes en España y a quienes viven en Venezuela.<br></p>
							<strong>¿Cuáles son los fines de LEAN?</strong><br>
							<p>LEAN está dedicada a fomentar valores e instaurarlos como principios,<br> trabajar en formación cívica, promover y defender las libertades<br> individuales y los derechos humanos en Venezuela, sensibilizar a la<br> gente sobre la importancia de conocer, respetar y practicar los principios<br> contenidos en la Declaración Universal de Derechos Humanos, asistir<br> a víctimas de actos violentos y persecución, favorecer la adquisición<br>de conocimiento a través de la lectura y trabajar incansablemente<br> en la ayuda humanitaria como gesto de solidaridad y buena voluntad.<br></p>
							<strong>¿Cuáles son las actividades de LEAN?</strong><br>
							<p>LEAN trabaja en el desarrollo y publicación de estudios de investigación<br> sobre temas de interés social, cultural, político y económico, preparación<br> de ponencias para foros y conferencias, presentación en eventos<br> especializados y mesas de trabajo, petición de colaboración a<br> personalidades de reconocida trayectoria, estudio de casos de violación<br> de derechos humanos a través de letrados voluntarios, asistencia y<br> representación para la defensa de las víctimas de actos violentos y<br> persecución, lanzamiento de campañas sobre valores cívicos y derechos<br> humanos, planificación y ejecución de programas de voluntariado para<br> brindar ayuda humanitaria y organización de charlas sobre la<br> situación económica, política y social de Venezuela y demás temas<br> de interés mundial.<br></p>
							<strong>Qu hacemos?</strong><br>
							<p>La asociación civil LEAN fue creada con el objetivo<br> de ayudar, a través de acciones concretas, a nuestros<br> conciudadanos en Venezuela ante la grave escasez de medicinas<br> e insumos médicos en que se encuentra el país. Nuestra<br> misión consiste en recolectar ayuda médico sanitaria en delegaciones en<br> España y, a través de agentes de transporte, llevarlos<br> a Venezuela para que otras asociaciones se encarguen de<br> su distribución. De esta manera aportamos nuestro granito de arena<br> ayudando a llevar asistencia humanitaria a Venezuela. Somos una asociación<br> sin fines de lucro, dedicada a la defensa de los<br> Derechos Humanos.</p></td><br>
